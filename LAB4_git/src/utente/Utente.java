package utente;

public class Utente {
	private String nome;
	private String genero;
	private int idade;
	private float altura;
	private int peso;
	public Utente(String nome, String genero, int idade, float altura, int peso) {
		this.nome = nome;
		this.genero = genero;
		this.idade = idade;
		this.altura = altura;
		this.peso = peso;
	}

}
